set1 = {'red', 'green', 'blue'}
set2 = {'cyan', 'green', 'blue', 'magenta', 'red'}

# a) Comparing the sets using each of the comparison operators
print("Comparing sets using comparison operators:")
print("set1 == set2:", set1 == set2)
print("set1 != set2:", set1 != set2)
print("set1 < set2:", set1 < set2)
print("set1 <= set2:", set1 <= set2)
print("set1 > set2:", set1 > set2)
print("set1 >= set2:", set1 >= set2)

# b) Combining the sets using each of the mathematical set operators
print("\nCombining sets using mathematical set operators:")
print("Union (set1 | set2):", set1 | set2)
print("Intersection (set1 & set2):", set1 & set2)
print("Difference (set1 - set2):", set1 - set2)
print("Symmetric Difference (set1 ^ set2):", set1 ^ set2)